// Function to find a specific celebrity by slug
export function getCelebrityBySlug(slug: string): CelebrityRealName | undefined {
  return celebrityRealNames.find(celebrity => celebrity.slug === slug);
}

// Function to get related celebrities (excluding the current one)
export function getRelatedCelebrities(currentId: number, limit: number = 4): CelebrityRealName[] {
  return celebrityRealNames
    .filter(celebrity => celebrity.id !== currentId)
    .sort(() => 0.5 - Math.random()) // Simple random shuffle
    .slice(0, limit);
}
